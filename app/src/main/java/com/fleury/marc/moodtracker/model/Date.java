package com.fleury.marc.moodtracker.model;

public class Date {

    private int mCurrentDate = 0;
    private int mDate = 0;

    public int getCurrentDate() {
        return mCurrentDate;
    }

    public void setCurrentDate(int currentDate) {
        mCurrentDate = currentDate;
    }

    public int getDate() {
        return mDate;
    }

    public void setDate(int date) {
        mDate = date;
    }
}
